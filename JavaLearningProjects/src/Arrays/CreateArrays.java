package Arrays;

public class CreateArrays {

	public static void main(String[] args) {
		int[] arr = new int[2];
		System.out.println(arr[0]);
		
		int arr1[] = {10, 2, 44};
		System.out.println(arr1[0]);
		
		//manual input value
		int arr2[] = new int[3];
		arr2[0] = 100;
		arr2[1] = 200;
		arr2[2] = 500;
		System.out.println(arr2[2]);

	}

}
